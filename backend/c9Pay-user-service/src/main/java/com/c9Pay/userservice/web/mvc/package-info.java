/**
 * 이 패키지는 Model-View-Controller (MVC) 패턴을 구현하는 클래스들을 포함하고 있다.
 * MVC 패턴은 소프트웨어 디자인 패턴으로, 사용자 인터페이스와 비즈니스 로직을 분리하여
 * 애플리케이션을 구조화하는 역할을 한다.
 * <p>
 * 모델(Model) 클래스들은 데이터와 비즈니스 로직을 관리하며, 애플리케이션의 상태와 동작을
 * 나타내는 역할을 한다.
 * <p>
 * 뷰(View) 클래스들은 사용자 인터페이스를 표현하고 화면을 렌더링하는 역할을 한다.
 * <p>
 * 컨트롤러(Controller) 클래스들은 사용자 입력을 처리하고 모델과 뷰 사이의 흐름을 관리한다.
 * 또한 비즈니스 로직과 사용자 인터페이스 간의 결합도를 낮춰준다.
 * <p>
 * MVC 패턴을 따르면 애플리케이션의 유지 보수와 확장이 용이해지며, 각 컴포넌트의 역할이 분명하게
 * 정의되므로 코드의 가독성이 높아진다.
 */
